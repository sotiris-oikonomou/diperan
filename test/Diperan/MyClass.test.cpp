#include <Global.test.hpp>

#include <Diperan/MyClass.hpp>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TEST(Diperan_MyClass, IsDefined)
{

	using diperan::MyClass ;

	{

		MyClass myClass ;

		EXPECT_FALSE(myClass.isDefined()) ;

	}

	{

		MyClass myClass = MyClass::Integer(123) ;

		EXPECT_TRUE(myClass.isDefined()) ;

	}

}

TEST(Diperan_MyClass, DoSomething)
{

	using diperan::MyClass ;

	{

		MyClass myClass ;

		myClass.doSomething() ;

		EXPECT_EQ(0, myClass.getInteger()) ;

	}

	{

		MyClass myClass = MyClass::Integer(123) ;

		myClass.doSomething() ;

		EXPECT_EQ(246, myClass.getInteger()) ;

	}

}

